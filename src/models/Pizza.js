class Pizza{
    Id;
    Nombre;  
    LibreGluten;
    Importe;
    Descripcion;
    constructor(Id,Nombre, LibreGluten, Importe, Descripcion) {
        this.Id             = Id;
        this.Nombre         = Nombre;
        this.LibreGluten    = LibreGluten;
        this.Importe        = Importe;
        this.Descripcion    = Descripcion;
    }
}


export default Pizza